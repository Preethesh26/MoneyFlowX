const mongoose = require('mongoose');

const bankSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
  purpose: {
    type: String,
    enum: ['Salary', 'Daily', 'Savings', 'SIP', 'EMI', 'Investment'],
    required: true,
  },
  balance: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('Bank', bankSchema);
